#include <iostream>

int main()
{
    std::cout << 5 << std::endl;       // print literal number `5`
    std::cout << 11.2 << std::endl;    // print literal number `11.2`
    std::cout << 'X' << std::endl;     // print literal character `X`
    std::cout << "Hello" << std::endl; // print literal string `Hello`

    return 0;
}
